export const generateWeatherData = (days = 14) => {
  const conditions = ['sunny', 'cloudy', 'rainy', 'partly-cloudy', 'stormy'];
  const icons = ['☀️', '☁️', '🌧️', '⛅', '⛈️'];
  
  return Array.from({ length: days }, (_, index) => {
    const date = new Date();
    date.setDate(date.getDate() + index - Math.floor(days / 2));
    const conditionIndex = Math.floor(Math.random() * conditions.length);
    
    return {
      date: date.toISOString().split('T')[0],
      temperature: Math.floor(Math.random() * 10) + 25, // Sri Lankan temperatures 25-35°C
      condition: conditions[conditionIndex],
      icon: icons[conditionIndex],
      humidity: Math.floor(Math.random() * 30) + 60, // Higher humidity for tropical climate
      windSpeed: Math.floor(Math.random() * 10) + 5
    };
  });
};

